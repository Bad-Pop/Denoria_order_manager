package fr.denoria.client.space.exceptions;

public class CandidatureException extends DenoriaException {

    public CandidatureException() {
        super();
    }

    public CandidatureException(String message) {
        super(message);
    }

    public CandidatureException(String message, Throwable cause) {
        super(message, cause);
    }

    public CandidatureException(Throwable cause) {
        super(cause);
    }
}
